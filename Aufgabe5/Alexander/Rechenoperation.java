/**
 * Interface fuer Rechenoperationen.
 * @author Alexander
 *
 */
public interface Rechenoperation {
	/**
	 * Fuehrt eine Rechenoperation auf den gegebenen Wert aus.
	 * @param x der Wert, mit dem gerechnet werden soll.
	 * @return Das Ergebnis der Rechnung als double.
	 */
	public double berechne(double x);
}
